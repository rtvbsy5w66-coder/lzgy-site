# ============================================================================
# UTILITY FUNCTIONS
# ============================================================================

print_header() {
    local title="$1"
    local width=80
    echo ""
    echo -e "${BOLD}${CYAN}$(printf '‚îÅ%.0s' $(seq 1 $width))${NC}"
    echo -e "${BOLD}${WHITE}$title${NC}"
    echo -e "${BOLD}${CYAN}$(printf '‚îÅ%.0s' $(seq 1 $width))${NC}"
    echo ""
}

print_section() {
    local title="$1"
    echo ""
    echo -e "${BOLD}${BLUE}‚ñ∂ $title${NC}"
    echo -e "${BLUE}$(printf '‚îÄ%.0s' $(seq 1 60))${NC}"
}

print_success() {
    echo -e "${GREEN}‚úì${NC} $1"
}

print_failure() {
    echo -e "${RED}‚úó${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}‚ö†${NC} $1"
}

print_info() {
    echo -e "${CYAN}‚Ñπ${NC} $1"
}

